// Helper function to get user by ID
export const getUserById = (id: string): User | undefined => {
  return mockUsers.find(user => user.id === id);
};

// Helper function to get route by ID
export const getRouteById = (id: string): Route | undefined => {
  return [...mockRoutes, ...mockPastRoutes].find(route => route.id === id);
};

// Helper function to enrich route with driver data
export const enrichRouteWithDriver = (route: Route): Route => {
  return {
    ...route,
    driver: getUserById(route.driverId),
  };
};

// Helper function to enrich ride with route and passenger data
export const enrichRide = (ride: Ride): Ride => {
  const route = getRouteById(ride.routeId);
  return {
    ...ride,
    route: route ? enrichRouteWithDriver(route) : undefined,
    passenger: getUserById(ride.passengerId),
  };
};

